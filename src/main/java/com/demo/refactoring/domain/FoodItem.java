package com.demo.refactoring.domain;

public class FoodItem {
    private String id;
    private Double price;

    public FoodItem(String id, Double price) {
        this.id = id;
        this.price = price;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }
}
